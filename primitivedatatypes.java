public class primitivedatatypes {

    public static void main(String[] args) {
        int a = 10;
        float b = 5.5f;
        char c = 'A';
        boolean d = true;

        System.out.println("Integer: " + a);
        System.out.println("Float: " + b);
        System.out.println("Character: " + c);
        System.out.println("Boolean: " + d);
    }
}


